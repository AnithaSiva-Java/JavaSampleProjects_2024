package switchctrls;

public class Sample_stringswitch {

	public static void main(String[] args) {

		String a="5";
		switch(a)
		{
		
		case "1":
			System.out.println("1st Month");
			break;
			
		case "2":
			System.out.println("2nd month");
			break;
			
		default:
			System.out.println("Random month");
			
		}
	}

}

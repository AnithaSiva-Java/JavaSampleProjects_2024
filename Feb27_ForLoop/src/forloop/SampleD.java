package forloop;

public class SampleD {

	public static void main(String[] args) {
//WHile Loop Factorial Series
			int a=1;
			int output=1;
			
			int x=5;
			
			while(a<=x)
			{
				//System.out.println("Fact of a is" +output);
			output=output*a;
			a=a+1;
			}
			System.out.println("The Factorial of x is" +output);
			
	}

}

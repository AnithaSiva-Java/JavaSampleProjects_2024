package forloop;

public class SampleC {

	public static void main(String[] args) {
//For Loop Factorial Series
			int a=1;
			int output=1;
			
			int x=5;
			
			for(a=1;a<=x;a=a+1)
			{
				//System.out.println("Fact of a is" +output);
			output=output*a;
			}
			System.out.println("The Factorial of x is" +output);
			
	}

}
